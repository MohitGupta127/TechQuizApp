    private void btEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btEditActionPerformed
           int count=cbSubject.getSelectedIndex();
        if(count==0)
        {
            JOptionPane.showMessageDialog(null,"Please choose a subject!","Error!!",JOptionPane.ERROR_MESSAGE);
            return;
        }
        count=cbExamId.getItemCount();
        if(count==0)
        {
            JOptionPane.showMessageDialog(null,"Please select an Exam Id!","Error!!",JOptionPane.ERROR_MESSAGE);
            return;
        }
        String subject=cbSubject.getSelectedItem().toString();
        String examid=cbExamId.getSelectedItem().toString();
        int ans;
        ans=JOptionPane.showConfirmDialog(null,"You have chosen \""+subject+ " \""+examid+ "\" paper!\nIs this OK?","Confirmation!",JOptionPane.YES_OPTION);
        
        if(ans==JOptionPane.YES_OPTION)
        {
            try{
                int totalQuest=ExamDao.getQuestionCountByExam(examid.trim());
                System.out.println("Examid: "+examid+" total question: "+totalQuest);
                ExamPojo exam=new ExamPojo(examid,subject,totalQuest);
                EditQuestionsFrame ttf=new EditQuestionsFrame(exam);
                ttf.setVisible(true);
                this.dispose();
            }
            catch(SQLException e){
                  System.out.println("yaha hai error");
            e.printStackTrace();
              
            JOptionPane.showMessageDialog(null,"Error while connecting to DB!","Error!!",JOptionPane.ERROR_MESSAGE);}
        }
    }//GEN-LAST:event_btEditActionPerformed

