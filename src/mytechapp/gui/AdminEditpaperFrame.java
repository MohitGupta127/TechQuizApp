    private void cbSubjectItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cbSubjectItemStateChanged
         if(evt.getStateChange()==ItemEvent.DESELECTED)
        return;
        boolean result=validateInput();
        if(result==false){
            JOptionPane.showMessageDialog(null,"Please choose a subject","Error!!",JOptionPane.ERROR_MESSAGE);
            return;
        }
        cbExamId.removeAllItems();
        try{
            if(ExamDao.isPaperSet(subjectname)==false){
                JOptionPane.showMessageDialog(null,"Sorry!No Exam set for "+subjectname,"SUbject Not FOund!",JOptionPane.ERROR_MESSAGE);
                return;
            }
            ArrayList<String> examlist=ExamDao.getExamIdByStudent(UserProfile.getUsername(),subjectname);
            if(examlist.isEmpty()){
                JOptionPane.showMessageDialog(null,"You have given all exam of "+subjectname,"No more Exam!",JOptionPane.WARNING_MESSAGE);
                return;
            }
            for(String s:examlist)
            cbExamId.addItem(s);

        }
        catch(SQLException ex){
            ex.printStackTrace();
            JOptionPane.showMessageDialog(null,"Error while connecting to DB!","Error!!",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_cbSubjectItemStateChanged

